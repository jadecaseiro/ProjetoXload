    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        try {
            atendimentoAlterar.setCpfCliente(txtCpfCliente.getText());
            atendimentoAlterar.setDataHora(txtDataHora.getText());
            atendimentoAlterar.setResolucao(txResolucao.getText());
            atendimentoAlterar.setCodProduto(txtCodigoProduto.getText());
            atendimentoAlterar.setDescricaoAtendimento(jTextArea3.getText());
            //RepositorioCliente.obterInstancia().atualizar(clienteAlterar);
            RepositorioAtendimentoConexao.obterInstancia().atualizar(atendimentoAlterar);
            JOptionPane.showMessageDialog(null, "Atendimento alterado com sucesso");
            this.dispose();
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex.getMessage());
            
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

